!-----------------------------------------------------------------------------
  use mod_param, only: wp
  private

!------------------------------------------------------------------------------
!Public
!------------------------------------------------------------------------------
  public :: ebasis3_gen
  public :: ebasis1_gen

contains
!------------------------------------------------------------------------------
!Procedures
!------------------------------------------------------------------------------


  subroutine ebasis3_gen(nalp,alphas)
!------------------------------------------------------------------------------
!ebasise_gen
!------------------------------------------------------------------------------
!This procedure will generate a ebasis input file with sets of 3 symbols.
!
!    Date        Programmer        Comments
!    06/09/2011  C. Blanton        Original Code
!
!------------------------------------------------------------------------------
    implicit none
!------------------------------------------------------------------------------
!Parameters
!------------------------------------------------------------------------------
    integer, parameter :: ifp=20
    integer, parameter :: ione=1
    integer, parameter :: zero = 0 
    integer, parameter :: maxtype = 2
    real(wp), parameter :: one = 1.0d0 

!------------------------------------------------------------------------------
!Input
!------------------------------------------------------------------------------
    integer, intent(in) :: nalp
    real(wp), intent(in) :: alphas(nalp)

!------------------------------------------------------------------------------
!Output
!------------------------------------------------------------------------------

!------------------------------------------------------------------------------
!Internals
!------------------------------------------------------------------------------
    integer :: i
    integer :: k

!------------------------------------------------------------------------------
!Begin Code
!------------------------------------------------------------------------------
    open(unit = ifp,file='ebasis.inp',action='write')
    write(ifp,*) 'NO. OF UNIQUE CENTERS'
    write(ifp,*) ione
    write(ifp,*) '---------------------------------------------'
    write(ifp,*) 'CENTER'
    write(ifp,*) '0. 0. 0.' 
    write(ifp,*) 'SYMBOLS'
    write(ifp,*)   nalp
    do i = 1, nalp
       k = mod(i,3)
          if (k == 1) then 
             write(ifp,*) ' S    1 '
          end if
          if (k == 2) then 
             write(ifp,*) ' P    1 '
          end if
          if (k == 0) then 
             write(ifp,*) ' D    1 ' 
          end if
          write(ifp,*) ione,alphas(i),one 
    end do
    close(ifp)

!------------------------------------------------------------------------------
!End Code
!------------------------------------------------------------------------------
  end subroutine ebasis3_gen

  subroutine ebasis1_gen(nalp,alphas)
!------------------------------------------------------------------------------
!ebasis_gen
!------------------------------------------------------------------------------
!This proccudure will generate a ebasis input file with all S.
!
!    Date        Programmer        Comments
!    06/09/2011  C. Blanton        Original Code
!
!------------------------------------------------------------------------------
    implicit none
!------------------------------------------------------------------------------
!Parameters
!------------------------------------------------------------------------------
    integer, parameter :: ifp=20
    integer, parameter :: ione=1
    integer, parameter :: zero = 0 
    integer, parameter :: maxtype = 2
    real(wp), parameter :: one = 1.0d0 

!------------------------------------------------------------------------------
!Input
!------------------------------------------------------------------------------
    integer, intent(in) :: nalp
    real(wp), intent(in) :: alphas(nalp)

!------------------------------------------------------------------------------
!Output
!------------------------------------------------------------------------------

!------------------------------------------------------------------------------
!Internals
!------------------------------------------------------------------------------
    integer :: i
    integer :: k

!------------------------------------------------------------------------------
!Begin Code
!------------------------------------------------------------------------------
    open(unit = ifp,file='ebasis.inp',action='write')
    write(ifp,*) 'NO. OF UNIQUE CENTERS'
    write(ifp,*) ione
    write(ifp,*) '---------------------------------------------'
    write(ifp,*) 'CENTER'
    write(ifp,*) '0. 0. 0.' 
    write(ifp,*) 'SYMBOLS'
    write(ifp,*)   nalp
