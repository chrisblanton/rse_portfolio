       write(ifp,*) ' S    1 '
       write(ifp,*) ione,alphas(i),one 
    end do
    close(ifp)

!------------------------------------------------------------------------------
!End Code
!------------------------------------------------------------------------------
  end subroutine ebasis1_gen

